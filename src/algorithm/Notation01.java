package algorithm;

public class Notation01 {

    public static void main(String[] args) {

        // 时间复杂度为 O(1)
        int n = 1000;
        System.out.println("Hey - your input is : " + n);

        // 时间复杂度为 O(1)
        int m = 1000;
        System.out.println("Hey - your input is : " + m);
        System.out.println("Hmm.. I'm doing more stuff with : " + m);
        System.out.println("And more: " + m);
    }
}
